    printf("Расстояние между автомобилями через %g ч: %g км", t, ((v1+v2)*t)+s);
  
/*
Решить линейное уравнение A·x + B = 0, 
заданное своими коэффициентами A и B 
(коэффициент A не равен 0). 
*/
  
    float b;
   
