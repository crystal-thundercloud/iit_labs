    printf("\nВведите значение угла a в градусах (0 < α < 360): ");
    scanf("%g", &a);
    
    if (a>0&&a<360) {
        a = (a*M_PI)/180; //использование константы Пи из библиотеки math.h
        printf("Значение угла a в радианах: %g", a);
    }
 
    else printf("Значение за границами");
  
/*
Дано значение угла α в радианах (0 < α < 2·π). 
Определить значение этого же угла в градусах, 
учитывая, что 180◦ = π радианов
*/
    
    printf("\n\nДано значение угла α в радианах (0 < α < 2·π).\nОпределить значение этого же угла в градусах,\nучитывая, что 180◦ = π радианов.");
    
    printf("\nВведите значение угла a в радианах (0 < α < 2*π): ");
    scanf("%g", &a);
    
    if (a>0&&a<2*M_PI) {
        a = (a*180)/M_PI; //использование константы Пи из библиотеки math.h
        printf("Значение угла a в градусах: %g", a);
    }
 
    else printf("Значение за границами");
/*
Известно, что X кг конфет стоит A рублей.
Определить, сколько стоит 1 кг и Y кг этих же конфет.
*/
    float x,y;
    
    printf("\n\nИзвестно, что X кг конфет стоит A рублей.\nОпределить, сколько стоит 1 кг и Y кг этих же конфет.");
  
    printf("\nСколько кг конфет? ");
    scanf("%g", &x);
    printf("Сколько стоит такое количество конфет? ");
    scanf("%g", &a);
    printf("Стоимость скольких кг конфет нужно посчитать? ");
    scanf("%g", &y);
    
    printf("Стоимость за 1 кг: %g", a/x);
    printf("\nСтоимость %g кг конфет: %g", y, y*(a/x));
  
/*
Скорость первого автомобиля V1 км/ч, второго — V2 км/ч, 
расстояние между ними S км. 
Определить расстояние между ними через T часов, 
если автомобили удаляются друг от друга. 
*/
  
    float v1, v2, s, t;
    
    printf("\n\nСкорость первого автомобиля V1 км/ч, второго — V2 км/ч,\nрасстояние между ними S км.\nОпределить расстояние между ними через T часов,\nесли автомобили удаляются друг от друга. .");
    
    printf("\nСкорость первого автомобиля (км/ч): ");
