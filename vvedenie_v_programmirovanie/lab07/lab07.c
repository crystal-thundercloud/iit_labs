    printf("Введите A (не равный нулю): ");
    scanf("%g", &a);
    printf("Введите B: ");
    scanf("%g", &b);
    
    if (a!=0) printf("Для уравнения %g*x+%g=0 x=%g", a, b, -b/a);
    else printf("Нельзя посчитать для a=0");
  
 /*
Найти решение системы линейных уравнений вида 
A1·x + B1·y = C1, 
A2·x + B2·y = C2
*/
  
